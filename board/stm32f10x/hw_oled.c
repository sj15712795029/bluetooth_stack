/******************************************************************************
 *	函数名:	hw_oled_init
 * 参数:  	NULL
 * 返回值: 	oled init结果
 * 描述:		OLED的初始化，采用软件模拟IIC来驱动SSD1306，用到的PIN为:
 				SSD1306_SDA_PIN-->IIC SDA,SSD1306_SCL_PIN-->IIC SCL
******************************************************************************/
uint8_t hw_oled_init()
{
    GPIO_InitTypeDef  GPIO_InitStructure;

    RCC_APB2PeriphClockCmd(SSD1306_PERIPH_CLK | RCC_APB2Periph_AFIO, ENABLE);
    GPIO_PinRemapConfig(GPIO_Remap_SWJ_NoJTRST,ENABLE);
    GPIO_InitStructure.GPIO_Pin = SSD1306_SCL_PIN|SSD1306_SDA_PIN;
    GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;
    GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
    GPIO_Init(SSD1306_SCL_GPIO, &GPIO_InitStructure);
    GPIO_SetBits(SSD1306_SCL_GPIO,SSD1306_SCL_PIN|SSD1306_SDA_PIN);

    /* 延时200ms */
    hw_delay_ms(200);

    hw_oled_write_byte(oled_cmd_set_multiplex_ratio,OLED_CMD);			/* 设置复用率为32 */
    hw_oled_write_byte(0x3F,OLED_CMD);

    hw_oled_write_byte(oled_cmd_set_display_offset,OLED_CMD);			/* 设置偏移量为0 */
    hw_oled_write_byte(0x00,OLED_CMD);

    hw_oled_write_byte(oled_cmd_set_start_line_lowset,OLED_CMD);		/* 设置起始行地址 */

    hw_oled_write_byte(oled_cmd_set_remap1,OLED_CMD);					/* 设置重映射 */

    hw_oled_write_byte(oled_cmd_set_com_remap_scan,OLED_CMD);		/* 设置从COM[N-1]扫描到COM0 */

    hw_oled_write_byte(oled_cmd_set_compin_hw_conf,OLED_CMD);		/* 设置列引脚硬件配置 */
    hw_oled_write_byte(0x12,OLED_CMD);

    hw_oled_write_byte(oled_cmd_contrast,OLED_CMD);						/* 设置对比度为 */
    hw_oled_write_byte(0xff,OLED_CMD);

    hw_oled_write_byte(oled_cmd_entire_display_on_with_ram,OLED_CMD);

    hw_oled_write_byte(oled_cmd_normal_display,OLED_CMD);				/* 设置为正常显示 */

    hw_oled_write_byte(oled_cmd_set_clk_div_osc_freq,OLED_CMD);		/* 设置显示时钟分频值/震荡频率 */
    hw_oled_write_byte(0x80,OLED_CMD);

    hw_oled_write_byte(oled_cmd_set_charge_pump,OLED_CMD);
    hw_oled_write_byte(0x14,OLED_CMD);

    hw_oled_write_byte(oled_cmd_display_on,OLED_CMD);					/* 启动显示 */

    hw_oled_clear();

    return HW_ERR_OK;

}


/******************************************************************************
 *	函数名:	protocol_iic_start
 * 参数:  	NULL
 * 返回值: 	NULL
 * 描述:		software模拟IIC起始信号，信号时序是
 				SCL高电平，SDA从高到低跳变
******************************************************************************/
static void protocol_iic_start()
{
    IIC_SCLK_HIGH;
    IIC_SDIN_HIGH;
    IIC_SDIN_LOW;
}

/******************************************************************************
 *	函数名:	protocol_iic_stop
 * 参数:  	NULL
 * 返回值: 	NULL
 * 描述:		software模拟IIC停止信号，信号时序是
 				SCL高电平，SDA从低到高跳变
******************************************************************************/
static void protocol_iic_stop()
{
    IIC_SCLK_HIGH;
    IIC_SDIN_LOW;
    IIC_SDIN_HIGH;

}

/******************************************************************************
 *	函数名:	protocol_iic_wait_ack
 * 参数:  		NULL
 * 返回值: 	NULL
 * 描述:		software模拟IIC等待应答
******************************************************************************/
static void protocol_iic_wait_ack()
{
    IIC_SCLK_HIGH ;
    IIC_SCLK_LOW;
}

/******************************************************************************
 *	函数名:	protocol_iic_write_byte
 * 参数:  	byte(IN)		-->要写的数据
 * 返回值: 	NULL
 * 描述:		软件模拟IIC写byte
******************************************************************************/
static void protocol_iic_write_byte(uint8_t byte)
{
    uint8_t i;
    uint8_t m,da;
    da=byte;
    IIC_SCLK_LOW;
    for(i=0; i<8; i++)
    {
        m=da;
        m=m&HIGH_BIT_MASK;
        if(m==HIGH_BIT_MASK)
        {
            IIC_SDIN_HIGH;
        }
        else IIC_SDIN_LOW;
        da=da<<1;
        IIC_SCLK_HIGH;
        IIC_SCLK_LOW;
    }

}

/******************************************************************************
 *	函数名:	protocol_iic_write_cmd
 * 参数:  	command(IN)		-->要写的command
 * 返回值: 	NULL
 * 描述:		软件模拟IIC写命令
******************************************************************************/
static void protocol_iic_write_cmd(uint8_t command)
{
    protocol_iic_start();
    protocol_iic_write_byte(SSD1306_SLAVE_ADDR | SSD1306_IIC_WRITE);
    protocol_iic_wait_ack();
    protocol_iic_write_byte(SSD1306_IIC_CMD_MASK);
    protocol_iic_wait_ack();
    protocol_iic_write_byte(command);
    protocol_iic_wait_ack();
    protocol_iic_stop();
}

/******************************************************************************
 *	函数名:	protocol_iic_write_data
 * 参数:  	data(IN)		-->要写的data
 * 返回值: 	NULL
 * 描述:		软件模拟IIC写数据
******************************************************************************/
static void protocol_iic_write_data(uint8_t data)
{
    protocol_iic_start();
    protocol_iic_write_byte(SSD1306_SLAVE_ADDR | SSD1306_IIC_WRITE);
    protocol_iic_wait_ack();
    protocol_iic_write_byte(SSD1306_IIC_DAT_MASK);
    protocol_iic_wait_ack();
    protocol_iic_write_byte(data);
    protocol_iic_wait_ack();
    protocol_iic_stop();
}

/******************************************************************************
 *	函数名:	hw_oled_set_pos
 * 参数:  		col(IN)		-->要写的列
 				page(IN)		-->要写的页
 * 返回值: 	NULL
 * 描述:		设置OLED要写的pos
******************************************************************************/
static void hw_oled_set_pos(uint8_t col,uint8_t page)
{
    hw_oled_write_byte(oled_cmd_set_page0_start+page,OLED_CMD);
    hw_oled_write_byte((col&0x0f) | oled_cmd_lower_col_start_addr,OLED_CMD);
    hw_oled_write_byte(((col&0xf0)>>4)|oled_cmd_higher_col_start_addr,OLED_CMD);
}

/******************************************************************************
 *	函数名:	hw_oled_write_byte
 * 参数:  		byte(IN)		-->要写的byte
 				cmd(IN)		-->是否是cmd
 * 返回值: 	NULL
 * 描述:		OLED写byte
******************************************************************************/
static void hw_oled_write_byte(uint8_t byte,uint8_t cmd)
{
    if(cmd)
    {
        /* 通过iic发送command */
        protocol_iic_write_cmd(byte);
    }
    else
    {
        /* 通过iic发送data */
        protocol_iic_write_data(byte);
    }
}

/******************************************************************************
 *	函数名:	hw_oled_10_pow
 * 参数:  		power(IN)		-->10的多少次方
 * 返回值: 	10的power次方值
 * 描述:		计算10的次方数
******************************************************************************/
static uint32_t hw_oled_10_pow(uint8_t power)
{
    uint32_t result = 1;
    while(power--)
    {
        result *= 10;
    }
    return result;
}

